DROP TABLE "SYSTEM"."T_SUPPLIERS";

CREATE TABLE "SYSTEM"."T_SUPPLIERS" 
(	
    "SUPPLIER_ID" NUMBER NOT NULL ENABLE, 
    "SUPPLIER_NAME" VARCHAR2(200 BYTE) NOT NULL ENABLE
);
   
DROP TABLE "SYSTEM"."T_ORDERS";

CREATE TABLE "SYSTEM"."T_ORDERS" 
(	
    "ORDER_ID" NUMBER NOT NULL ENABLE, 
    "SUPPLIER_ID" NUMBER NOT NULL ENABLE, 
    "ORDER_DATE" DATE, 
    "ORDER_DESC" VARCHAR2(200 BYTE)
);

