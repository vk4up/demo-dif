SELECT 
    T_SUPPLIERS.SUPPLIER_ID, T_SUPPLIERS.SUPPLIER_NAME, T_ORDERS.ORDER_DATE
FROM 
    T_SUPPLIERS INNER JOIN T_ORDERS
    ON T_SUPPLIERS.SUPPLIER_ID = T_ORDERS.SUPPLIER_ID;

